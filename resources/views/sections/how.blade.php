        <h3 class="fw-bold  fs-2">How does a home solar power system work?</h3>
        <p class="text-muted mb-3">
          Traditionally most residential solar power systems were simply solar panels and an inverter. You don’t need to store the solar power for night use because net metering laws give you a credit for excess solar you export to the public grid and these credits cover the nighttime power you need to buy. These systems are known as “grid connect” or “grid-tied” solar systems.     This video shows how these systems operate over the cycle of a day. However, with time of use electric billing coming in soon more people are starting to add energy storage batteries to their solar system. This is not only to store excess solar power but also to charge at off-peak times and provide a source of power during expensive peak times. These systems don’t yet offer the quick payback grid-tied systems do but are cool if you can afford to spend a little more.
